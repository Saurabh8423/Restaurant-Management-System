import Order from "../models/orderModel.js";
import Chef from "../models/chefModel.js";
import Table from "../models/tableModel.js";

// Assign chef with fewer handled orders
const assignChef = async () => {
  try {
    // Sort chefs by least ordersHandled
    const chefs = await Chef.find().sort({ ordersHandled: 1 });
    if (chefs.length === 0) return null;

    const assignedChef = chefs[0];
    // Increment their order count
    assignedChef.ordersHandled += 1;
    await assignedChef.save();

    return assignedChef.name;
  } catch (err) {
    console.error("Error assigning chef:", err.message);
    return null;
  }
};

// Get all orders
export const getOrders = async (req, res) => {
  try {
    const orders = await Order.find().sort({ createdAt: -1 });
    res.json(orders);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

// Create new order
export const createOrder = async (req, res) => {
  try {
    let {
      orderId,
      type,
      items,
      totalAmount,
      tableNumber,
      clientName,
      phoneNumber,
      address,
      instructions,
      totals,
      user,
    } = req.body;

    // Handle total amount properly
    if (!totalAmount && totals?.grandTotal) {
      totalAmount = Number(totals.grandTotal);
    }

    // Normalize item format
    const mappedItems = (items || []).map((it) => ({
      name: it.name ?? it.id ?? "Item",
      quantity: it.quantity ?? it.qty ?? 1,
      price: it.price ?? it.unitPrice ?? 0,
    }));

    // ✅ Assign chef automatically
    const assignedChef = await assignChef();

    // Create order in DB
    const newOrder = await Order.create({
      orderId: orderId || `ODR-${Date.now()}`,
      type: type || "Takeaway",
      tableNumber: tableNumber ?? null,
      items: mappedItems,
      totalAmount:
        Number(totalAmount) ||
        mappedItems.reduce((sum, i) => sum + i.quantity * i.price, 0),
      clientName: clientName ?? user?.name ?? "",
      phoneNumber: phoneNumber ?? user?.phone ?? "",
      address: address ?? user?.address ?? "",
      instructions: instructions ?? "",
      assignedChef,
      status: "Processing",
      processingTime: Math.floor(Math.random() * 10) + 5, // random countdown in minutes
    });

    // ✅ If it's a dine-in order, mark table reserved
    if (
      (type === "Dine In" || type === "DineIn" || req.body.orderType === "Dine In") &&
      tableNumber
    ) {
      try {
        await Table.findOneAndUpdate(
          { tableNumber: Number(tableNumber) },
          { reserved: true, $inc: { bookedFor: 1 } },
          { new: true }
        );
      } catch (e) {
        console.warn("Table reservation failed:", e.message);
      }
    }

    res.status(201).json(newOrder);
  } catch (error) {
    console.error("Order creation error:", error.message);
    res.status(400).json({ message: error.message });
  }
};

// Update order status
export const updateOrderStatus = async (req, res) => {
  try {
    const { id } = req.params;
    const { status } = req.body;

    const order = await Order.findByIdAndUpdate(id, { status }, { new: true });
    res.json(order);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};
